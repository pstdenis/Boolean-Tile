def make_tt(idx):
    """8-row truth table for 3-variable function index idx (0..255)."""
    return tuple((idx >> (7 - i)) & 1 for i in range(8))

def walsh_8(tt):
    """8-point Walsh-Hadamard transform (unnormalized)."""
    h2 = np.array([[1,1],[1,-1]], dtype=int)
    h4 = np.kron(h2, h2)
    h8 = np.kron(h4, h2)
    return tuple(int(x) for x in h8 @ np.array(tt, dtype=int))

