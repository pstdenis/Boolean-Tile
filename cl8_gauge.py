def grade(w):
    """Grade of a function: number of non-zero non-DC Walsh coeffs mod 2."""
    return sum(1 for i in range(1, 8) if w[i] != 0) % 2

def clifford_mul(w1, w2):
    """Twisted group algebra product: sign = (-1)^{g1*g2}."""
    g1, g2 = grade(w1), grade(w2)
    # The product w1 XOR w2 in Walsh space requires the Walsh transform
    # of the XOR. But the Clifford product in function space is:
    # f * g = (f XOR g) * (-1)^{grade(f)*grade(g)}
    sign = -1 if (g1 == 1 and g2 == 1) else 1
    return sign

